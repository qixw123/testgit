package cn.redcdn.jec.group.dto;

public class ContactDto
{

	/**
     * id
     */
    private String id;
    
	/**
     * name
     */
    private String name;
    
	/**
     * nube
     */
    private String nube;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNube() {
		return nube;
	}

	public void setNube(String nube) {
		this.nube = nube;
	}
}
