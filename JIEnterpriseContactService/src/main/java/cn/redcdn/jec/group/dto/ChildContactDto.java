package cn.redcdn.jec.group.dto;

public class ChildContactDto
{

	/**
     * id
     */
    private String id;
    
	/**
     * name
     */
    private String name;
    
	/**
     * num
     */
    private String num;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNum() {
		return num;
	}

	public void setNum(String num) {
		this.num = num;
	}
}
