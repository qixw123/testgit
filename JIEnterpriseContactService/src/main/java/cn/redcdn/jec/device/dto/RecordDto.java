package cn.redcdn.jec.device.dto;

public class RecordDto
{
	/**
     * fileName
     */
    private String fileName;
    
	/**
     * nube
     */
    private String nube;
    
	/**
     * recoder
     */
    private String recoder;
    
	/**
     * meetingId
     */
    private String meetingId;
    
	/**
     * recordTimeSpan
     */
    private String recordTimeSpan;
    
	/**
     * fileSize
     */
    private String fileSize;
    
	/**
     * startRecordTime
     */
    private String startRecordTime;
    
	/**
     * recordFile
     */
    private String recordFile;

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public String getNube() {
		return nube;
	}

	public void setNube(String nube) {
		this.nube = nube;
	}

	public String getRecoder() {
		return recoder;
	}

	public void setRecoder(String recoder) {
		this.recoder = recoder;
	}

	public String getMeetingId() {
		return meetingId;
	}

	public void setMeetingId(String meetingId) {
		this.meetingId = meetingId;
	}

	public String getRecordTimeSpan() {
		return recordTimeSpan;
	}

	public void setRecordTimeSpan(String recordTimeSpan) {
		this.recordTimeSpan = recordTimeSpan;
	}

	public String getFileSize() {
		return fileSize;
	}

	public void setFileSize(String fileSize) {
		this.fileSize = fileSize;
	}

	public String getStartRecordTime() {
		return startRecordTime;
	}

	public void setStartRecordTime(String startRecordTime) {
		this.startRecordTime = startRecordTime;
	}

	public String getRecordFile() {
		return recordFile;
	}

	public void setRecordFile(String recordFile) {
		this.recordFile = recordFile;
	} 
    
}
